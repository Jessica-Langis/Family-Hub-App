export function pickDailyIndex(arr, offset = 0) {
  const day = new Date().toDateString()
  let hash = 0
  for (let i = 0; i < day.length; i++) hash = (hash * 31 + day.charCodeAt(i)) % arr.length
  return ((hash + offset) % arr.length + arr.length) % arr.length
}
